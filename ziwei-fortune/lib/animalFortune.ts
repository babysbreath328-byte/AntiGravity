export function getAnimalResult(year: number, month: number, day: number): AnimalResult {
  const yearData = YEAR_MONTH_TABLE[year];
  if (!yearData) {
    throw new Error(`動物占い（個性心理学）の対応範囲は1938〜2030年です（入力: ${year}年）`);
  }
  const base = yearData[month - 1];
  let total = base + day;
  if (total > 60) total -= 60;

  const char = CHARACTERS_60[total - 1];
  const text = ANIMAL_TEXT[char.animal];

  return {
    animalName: char.animal,
    fullName: char.fullName,
    personality: text.personality,
    strengths: text.strengths,
    weaknesses: text.weaknesses,
    characterNo: total,
    group: char.group,
    starType: char.starType,
  };
}
